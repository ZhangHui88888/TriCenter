    /**
     * 按阶段分组获取需求项
     */
    public static LinkedHashMap<String, List<RequirementItem>> getGroupedByPhase() {
        LinkedHashMap<String, List<RequirementItem>> grouped = new LinkedHashMap<>();
        for (String phase : PHASES) {
            grouped.put(phase, new ArrayList<>());
        }
        for (RequirementItem item : ALL_REQUIREMENTS) {
            grouped.computeIfAbsent(item.phase, k -> new ArrayList<>()).add(item);
        }
        return grouped;
    }
